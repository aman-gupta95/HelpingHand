    private void addAccoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAccoBtnActionPerformed
        // TODO add your handling code here:
        String accoName = accoNameTxtField.getText();
        String accoLocation = accoLocationTxtField.getText();
        int spots = Integer.parseInt(spotsTxtField.getText());
        ArrayList<Accomodation> accoDirect = enterprise.getAccomodationDirectory().getAccomodationDirectory();
        for(Accomodation acco: accoDirect){
            if(acco.getAccomodationName()==accoName && acco.getLocation()==accoLocation){
                acco.setNoOfSpots(acco.getNoOfSpots()+spots);
            }else{
                Accomodation accomodation = new Accomodation(accoName,  accoLocation, spots);
                enterprise.getAccomodationDirectory().getAccomodationDirectory().add(accomodation);
            }
            populateAccoTbl();
        }
        publishChanges();
    }//GEN-LAST:event_addAccoBtnActionPerformed
    private void publishChanges(){
        for(Network net: system.getNetworkList()){
            for(Enterprise ent: net.getEnterpriseDirectory().getEnterpriseList()){
                if(ent.getEnterpriseType().equals(Enterprise.EnterpriseType.HelpCenter)){
                    HelpCenterEnterprise helpcenter = (HelpCenterEnterprise)ent;
                    for(Accomodation acco: enterprise.getAccomodationDirectory().getAccomodationDirectory()){
                                if(helpcenter.getAccomodationDirectory().getAccomodationDirectory().indexOf(acco)== -1)
                                helpcenter.getAccomodationDirectory().getAccomodationDirectory().add(acco);
                            }
                        }
                    }
                }
            }
    private void unpublishAccoBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unpublishAccoBtnActionPerformed
        // TODO add your handling code here:
        if(accoTbl.getSelectedRow()<0){
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }
        Accomodation acco = (Accomodation)accoTbl.getValueAt(accoTbl.getSelectedRow(), 0);
        enterprise.getAccomodationDirectory().getAccomodationDirectory().remove(acco);
        populateAccoTbl();
        publishChanges();
    }//GEN-LAST:event_unpublishAccoBtnActionPerformed

