    private void populateAccoTbl(){
        DefaultTableModel model = (DefaultTableModel)accoTbl.getModel();
        model.setRowCount(0);
        Object row[] = new Object[3];
        
        for(Accomodation acco: enterprise.getAccomodationDirectory().getAccomodationDirectory()){
            row[0] = acco;
            row[1] = acco.getLocation();
            row[2] = acco.getNoOfSpots();
            model.addRow(row);
        }
    }
