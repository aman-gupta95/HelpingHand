    private void populatePrescriptionTable(){
        DefaultTableModel model = (DefaultTableModel)prescriptionTable.getModel();
        model.setRowCount(0);
        Object []row = new Object[2];
        for(Map.Entry mapElement: request.getPrescription().entrySet()){
            row[0] = mapElement.getKey();
            row[1] = mapElement.getValue();
            model.addRow(row);
        }
    }
    
    private void populateComboBox(){
        drugComboBox.removeAllItems();
        for(Drug med : enterprise.getDrugList().values()){
            drugComboBox.addItem(med.getName());
        }
    }
    private void addPrescriptionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPrescriptionBtnActionPerformed
        // TODO add your handling code here:
        String medicine = (String)drugComboBox.getSelectedItem();
        Integer quantity = Integer.parseInt(quantityTxtField.getText());
        request.getPrescription().put(medicine, quantity);
        populatePrescriptionTable();
    }//GEN-LAST:event_addPrescriptionBtnActionPerformed
    
    private void removeDrugBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeDrugBtnActionPerformed
        // TODO add your handling code here:
        String medicine = String.valueOf(prescriptionTable.getValueAt(prescriptionTable.getSelectedRow(),0));
        request.getPrescription().remove(medicine);
        populatePrescriptionTable();
    }//GEN-LAST:event_removeDrugBtnActionPerformed


