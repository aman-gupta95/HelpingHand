    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        
        WorkRequest.WorkStatusType type = WorkRequest.WorkStatusType.Prescribed;
        
        request.setSender(userAccount);
        request.setRequestDate(new Date());
        request.setTestStatus(type.getValue());
        request.setHomeless(homeless);
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof NurseOrganization){
                org = organization;
                if(medReq){
                    org.getWorkQueue().getWorkRequestList().add(request);
                    userAccount.getWorkQueue().getWorkRequestList().add(request);
                }else{
                    org.getWorkQueue().getWorkRequestList().set(org.getWorkQueue().getWorkRequestList().indexOf(request), request);
                    userAccount.getWorkQueue().getWorkRequestList().set(userAccount.getWorkQueue().getWorkRequestList().indexOf(request), request);
                }
                break;
            }
        }
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof DoctorOrganization){
                org = organization;
                for(Iterator<WorkRequest> iterator = org.getWorkQueue().getWorkRequestList().iterator(); iterator.hasNext();){
                    WorkRequest req = iterator.next();
                    if(req.getHomeless() == homeless){
                        iterator.remove();
                    }
                }
                break;
            }
        }
        
        
        
        JOptionPane.showMessageDialog(null, "Prescription sent");
    }//GEN-LAST:event_submitBtnActionPerformed

