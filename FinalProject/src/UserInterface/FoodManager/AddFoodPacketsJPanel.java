    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        int batchId = Integer.parseInt(batchIDtxtField.getText());
        int senderID = Integer.parseInt(senderIDtxtField.getText());
        Date sentDate = dateSent.getDate();
        Date expDate = dateOfExpiry.getDate();
        int quantity = Integer.parseInt(quantityTxtField.getText());
        
        FoodPackets foodPacket = new FoodPackets(senderID, sentDate, expDate, quantity);
        foodEnterprise.getFoodPacketDirectory().getFoodPacketDirectory().add(foodPacket);
        
        batchIDtxtField.setText(String.valueOf(batchId));
        senderIDtxtField.setText(String.valueOf(senderID));
        dateSent.setDate(sentDate);
        dateOfExpiry.setDate(expDate);
        quantityTxtField.setText(String.valueOf(quantity));
        JOptionPane.showMessageDialog(null, "Food Packet Added!");

        populateFoodPackets();

    }//GEN-LAST:event_addBtnActionPerformed

