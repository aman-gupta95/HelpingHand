    public void populateFoodPackets() {
        DefaultTableModel model = (DefaultTableModel) foodPacketTbl.getModel();
        model.setRowCount(0);

        for (FoodPackets foodPacket : foodEnterprise.getFoodPacketDirectory().getFoodPacketDirectory()) {
            Object[] row = new Object[5];
            row[0] = Integer.parseInt(batchIDtxtField.getText());
            row[1] = foodPacket;
            row[2] = foodPacket.getSent();
            row[3] = foodPacket.getExpiry();
            row[4] = foodPacket.getQuantity();
            model.addRow(row);

        }
    }

