    private void populateInventoryTable(){
        DefaultTableModel model = (DefaultTableModel)inventoryTbl.getModel();
        DateFormat df = new SimpleDateFormat("MM/dd/yyyy");
        Date todayDate = new Date();
        try{
            todayDate = df.parse(df.format(todayDate));
        }catch(Exception e){
            System.out.println(e);
        }
//        
        model.setRowCount(0);
        Object []row = new Object[4];
        for(Drug drug: enterprise.getDrugList().values()){
                Date drugDate = new Date();
                try{
                    drugDate = df.parse(df.format(drug.getDate()));
                }catch(Exception e){
                    System.out.println(e);
                }
                if(filterComboBox.getSelectedItem().toString().equals("Low on Stock")&&drug.getQuantity()<=5){
                    row[0] = drug;
                    row[1] = drug.getName();
                    row[2] = drug.getQuantity();
                    row[3] = drug.getDate();
                    model.addRow(row);
                }else if(filterComboBox.getSelectedItem().equals("Expired Drugs")&& todayDate.after(drugDate)){
                    row[0] = drug;
                    row[1] = drug.getName();
                    row[2] = drug.getQuantity();
                    row[3] = drug.getDate();
                    model.addRow(row);
                }else if(filterComboBox.getSelectedItem().equals("All drugs")){
                    row[0] = drug;
                    row[1] = drug.getName();
                    row[2] = drug.getQuantity();
                    row[3] = drug.getDate();
                    model.addRow(row);
                }
        }
    }
    
    private void populateRequestsTable(){
        DefaultTableModel model = (DefaultTableModel)requestTbl.getModel();
        model.setRowCount(0);
        Object []row = new Object[4];
        
        for(WorkRequest request: account.getWorkQueue().getWorkRequestList()){
            if(request instanceof SupplyDrugsWorkRequest){
               SupplyDrugsWorkRequest supply = (SupplyDrugsWorkRequest)request;
               Drug drug = supply.getDrug();
               row[0] = drug;
               row[1] = drug.getName();
               row[2] = supply.getQuantity();
               row[3] = supply.getTestStatus();
               model.addRow(row);   
            }
        }
    }
