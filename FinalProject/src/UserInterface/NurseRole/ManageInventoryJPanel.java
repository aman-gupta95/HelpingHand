    private void raiseRequestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_raiseRequestBtnActionPerformed
        // TODO add your handling code here:
        Drug drug = (Drug)inventoryTbl.getValueAt(inventoryTbl.getSelectedRow(), 0);
        SupplyDrugsWorkRequest drugRequest = new SupplyDrugsWorkRequest();
        int quantity = Integer.parseInt(quantityTxtField.getText());
        WorkRequest.WorkStatusType type = WorkRequest.WorkStatusType.Pending;
        drugRequest.setTestStatus(type.getValue());
        drugRequest.setDrug(drug);
        drugRequest.setQuantity(quantity);
        drugRequest.setSender(account);
        drugRequest.setEnterprise(enterprise);
        try{drugRequest.setRequestDate(df.parse(String.valueOf(new Date())));}
        catch(Exception e){
            System.out.println(e);
        }
        account.getWorkQueue().getWorkRequestList().add(drugRequest);
        for( Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(org instanceof RelationManagerOrganization){
                org.getWorkQueue().getWorkRequestList().add(drugRequest);
                break;
            }
        }
        populateRequestsTable();
    }//GEN-LAST:event_raiseRequestBtnActionPerformed

    private void discardBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_discardBtnActionPerformed
        // TODO add your handling code here:
        Date todayDate = new Date();
        try{
            todayDate = this.df.parse(df.format(todayDate));
        }catch(ParseException e){
                System.out.println(e);
            }
        Drug drug = (Drug)inventoryTbl.getValueAt(inventoryTbl.getSelectedRow(), 0);
        drug.setQuantity(0);
        drug.setDate(todayDate);
        enterprise.getDrugList().put(drug.getId(), drug);
        populateInventoryTable();
    }//GEN-LAST:event_discardBtnActionPerformed

