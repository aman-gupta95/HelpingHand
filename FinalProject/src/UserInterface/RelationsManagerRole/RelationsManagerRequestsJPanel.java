    private void populateRequestsTable(){
        DefaultTableModel model = (DefaultTableModel)requestsTbl.getModel();
        model.setRowCount(0);
        Object []row = new Object[4];
        for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
            if(org instanceof RelationManagerOrganization){
                organization = (RelationManagerOrganization)org;
            }
        }
        for(WorkRequest request: organization.getWorkQueue().getWorkRequestList()){
            if(requestTypeComboBox.getSelectedItem().toString() == "Supply Drugs Requests" && (request instanceof SupplyDrugsWorkRequest)){
                row[0] = request;
                row[1] = request.getSender();
                row[2] = request.getRequestDate();
                row[3] = WorkRequest.WorkStatusType.Pending.getValue();
                model.addRow(row);
            }else if(requestTypeComboBox.getSelectedItem().toString() == "Lab Test Requests" && (request instanceof LabTestWorkRequest)){
                row[0] = request;
                row[1] = request.getSender();
                row[2] = request.getRequestDate();
                row[3] = WorkRequest.WorkStatusType.Pending.getValue();
                model.addRow(row);
            }
        }
    }

