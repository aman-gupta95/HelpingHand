    private void publishBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_publishBtnActionPerformed
        // TODO add your handling code here:
        for(Network network: system.getNetworkList()){
            for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList()){
                if(this.enterprise == enterprise){
                        for(Enterprise ent: network.getEnterpriseDirectory().getEnterpriseList()){
                            if(ent instanceof HospitalEnterprise){
                                for(int i=0;i<requestsTbl.getRowCount();i++){
                                    ent.getWorkQueue().getWorkRequestList().add((WorkRequest)requestsTbl.getValueAt(i, 0));
                                    this.organization.getWorkQueue().getWorkRequestList().remove((WorkRequest)requestsTbl.getValueAt(i, 0));
                                }
                                DefaultTableModel model = (DefaultTableModel)requestsTbl.getModel();
                                model.setRowCount(0);
                                break;
                            }
                        }
                        break;
                }
                break;
            }
        }
    }//GEN-LAST:event_publishBtnActionPerformed

